#include<stdio.h>
#include<conio.h>

int main()
{
	int result;
	
	result = 7 > 4;
	printf("%d \n", result);
	
	result = 7 < 4;
	printf("%d \n", result);
	
	result = 4 == 4;
	printf("%d \n", result);

	result = 4 != 4;
	printf("%d \n", result);	
	
	result = 4 >= 4;
	printf("%d \n", result);
	
	
	// Relational Operators are:  >, <, >=, <=, ==, !=
	
	
	return 0;
}
