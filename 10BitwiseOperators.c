#include<stdio.h>

int main()
{
	// Bitwise OR --> |
	
	int a = 25;				// 25 --> 1 1 0 0 1
	int b = 15;				// 15 --> 0 1 1 1 1
							// --------------------------
	int c = a | b;			//        1 1 1 1 1  --> 31
	
	printf("%d \n",c);
	

	// Bitwise AND --> &
	
							// 25 --> 1 1 0 0 1
							// 15 --> 0 1 1 1 1
							// --------------------------
	c = a & b;				//        0 1 0 0 1  --> 9
	
	printf("%d",c);
		
	
}
